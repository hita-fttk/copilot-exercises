package copilot.exercises.ex05.p01;

public class Device {
    /* DeviceクラスのpowerOnメソッドは、"Device is powered on."と表示します
- DeviceクラスのuseDeviceメソッドは、"Device is used."と表示します */
    public void powerOn() {
        System.out.println("Device is powered on.");
    }

    public void useDevice() {
        System.out.println("Device is used.");
    }
}
