public class Ex01 {
    //2 つの変数 num1 と num2 を宣言し、それぞれ整数値を代入してください。
    //その後、それらの変数の値を表示してください。  例： num1 = 10, num2 = 20
    public static void main(String[] args) {
        int num1 = 10;
        int num2 = 20;
        //表示する際、計算結果と演算子を組み合わせて例えば「5 + 3 = 8」という形式で出力してください
        //num1 と num2 の足し算、引き算、掛け算、割り算の結果をそれぞれ表示してください
        System.out.println(num1 + " + " + num2 + " = " + (num1 + num2));
        System.out.println(num1 + " - " + num2 + " = " + (num1 - num2));
        System.out.println(num1 + " * " + num2 + " = " + (num1 * num2));
        System.out.println(num1 + " / " + num2 + " = " + (num1 / num2));
    }

  

}
